x = int(input('First number: '))
y = int(input('Second number: '))
operation = input('Operation(plus, minus, multiply and divide): ')

if operation ==('plus'):
    print(x + y)
if operation == ('minus'):
    print(x - y)
if operation == ('multiply'):
    print(x * y)
if operation == ('divide'):
    print(x / y)
    
